package com.javerianaaes.coordinador;

public class Canales {

	private String canalType;
	private String userReference;
	private String typePublicService;
	
	

	public String getCanalType() {
		return canalType;
	}

	public void setCanalType(String canalType) {
		this.canalType = canalType;
	}

	public String getUserReference() {
		return userReference;
	}

	public void setUserReference(String userReference) {
		this.userReference = userReference;
	}

	public String getTypePublicService() {
		return typePublicService;
	}

	public void setTypePublicService(String typePublicService) {
		this.typePublicService = typePublicService;
	}
	
	

}
